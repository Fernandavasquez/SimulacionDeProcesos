public class controlR {
    private String P;
    private String TC;
    private String TL;

    public controlR() {
        this.P = P;
        this.TC = TC;
        this.TL = TL;
    }

    public String getP() {
        return P;
    }

    public void setP(String P) {
        this.P = P;
    }

    public String getTC() {
        return TC;
    }

    public void setTC(String TC) {
        this.TC = TC;
    }

    public String getTL() {
        return TL;
    }

    public void setTL(String TL) {
        this.TL = TL;
    }
    
}